import java.util.*;

public class Main {
	
	public static void main(String[] args) {
		
		Scanner scanner = new Scanner(System.in);
		
		int N = scanner.nextInt();
		String sum = "";
		for(int i=0; i<N; i++) {
			
			sum = sum + "*";
			System.out.println(sum);
			
		}
		
		
		
	}
	
}